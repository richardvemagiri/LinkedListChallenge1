
public class Song {
	
	private String Title;
	private String Duration;
	
	//Getters and Setters
	
	public String getTitle() {
		return Title;
	}
	public void setTitle(String title) {
		Title = title;
	}
	public String getDuration() {
		return Duration;
	}
	public void setDuration(String duration) {
		Duration = duration;
	}
	
	
	
	
}
